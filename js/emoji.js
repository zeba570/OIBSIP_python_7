// Build emoji picker content
function buildEmojiPicker() {
  const picker = document.getElementById('emojiPicker');
  picker.innerHTML = '';
  Object.entries(EMOJI_DATA).forEach(([cat, emojis]) => {
    const catEl = document.createElement('div');
    catEl.className = 'emoji-category';
    catEl.textContent = cat;
    picker.appendChild(catEl);
    const grid = document.createElement('div');
    grid.className = 'emoji-grid';
    emojis.forEach(e => {
      const btn = document.createElement('button');
      btn.className = 'emoji-btn';
      btn.textContent = e;
      btn.onclick = () => insertEmoji(e);
      grid.appendChild(btn);
    });
    picker.appendChild(grid);
  });
}
 
function insertEmoji(emoji) {
  const ta = document.getElementById('messageInput');
  const pos = ta.selectionStart;
  ta.value = ta.value.slice(0, pos) + emoji + ta.value.slice(pos);
  ta.selectionStart = ta.selectionEnd = pos + emoji.length;
  ta.focus();
}
 
function toggleEmojiPicker() {
  const picker = document.getElementById('emojiPicker');
  picker.classList.toggle('hidden');
  if (!picker.classList.contains('hidden') && !picker.hasChildNodes()) {
    buildEmojiPicker();
  }
}
 
// Close emoji picker when clicking outside
document.addEventListener('click', (e) => {
  const picker = document.getElementById('emojiPicker');
  if (picker && !picker.classList.contains('hidden') &&
      !picker.contains(e.target) && !e.target.closest('[onclick="toggleEmojiPicker()"]')) {
    picker.classList.add('hidden');
  }
});